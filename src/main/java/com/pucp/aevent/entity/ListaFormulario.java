import java.util.List;

public class ListaFormulario {
	 private List<Long> listaPregunta;
	 private List<Long> listaSeccion;
	 private List<Long> listaDivision;
	public List<Long> getListaPregunta() {
		return listaPregunta;
	}
	public void setListaPregunta(List<Long> listaPregunta) {
		this.listaPregunta = listaPregunta;
	}
	public List<Long> getListaSeccion() {
		return listaSeccion;
	}
	public void setListaSeccion(List<Long> listaSeccion) {
		this.listaSeccion = listaSeccion;
	}
	public List<Long> getListaDivision() {
		return listaDivision;
	}
	public void setListaDivision(List<Long> listaDivision) {
		this.listaDivision = listaDivision;
	}
	
	 
}
